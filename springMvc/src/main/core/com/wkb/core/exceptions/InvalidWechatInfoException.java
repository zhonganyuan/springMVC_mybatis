package com.wkb.core.exceptions;

/**
 * 获取微信信息失败异常
 * @author huanglt
 *
 */
public class InvalidWechatInfoException extends Exception {

	private static final long serialVersionUID = 1L;
	
	public InvalidWechatInfoException(){
		super();
	}
	
	public InvalidWechatInfoException(String mes){
		super(mes);
	}
	
	public InvalidWechatInfoException(Throwable cause){
		super(cause);
	}
	
	public InvalidWechatInfoException(String mes,Throwable cause){
		super(mes,cause);
	}

}
